class Main {
    public static void main(String[] args) {
        printNumber(5, 10);
    }

    static void printNumber(int a, int b) {
        for (int i = 0; i < a; i++) {
            System.out.println(i);
        }

        for (int k = 0; k < b; k++) {
            System.out.println(k);
        } 
    }
}